package domain;

public class Response {
	
	String mensaje;
	boolean error;
	
	public Response() {
		
	}
	
	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}

	public boolean isError() {
		return error;
	}

	public void setError(boolean error) {
		this.error = error;
	}
	
	
	
	
	
	
	
	

}
