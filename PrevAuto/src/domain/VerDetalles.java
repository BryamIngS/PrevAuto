package domain;

public class VerDetalles {
	
	private int idVehiculo;
	private String nombre;
	private String modelo;
	private String marca;
	private String placa;
	
	
	public VerDetalles(int idVehiculo, String nombre, String modelo, String marca, 
			String placa) {
		
		this.idVehiculo = idVehiculo;
		this.nombre = nombre;
		this.modelo = modelo;
		this.marca = marca;
		this.placa = placa;
	}
	
	
	
}
